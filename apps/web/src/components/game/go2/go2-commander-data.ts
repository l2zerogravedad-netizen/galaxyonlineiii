export const COMMANDER_IDS = COMMANDERS.map((c) => c.id);

export function getCommanderById(id: string): Commander | undefined {
  return COMMANDERS.find((c) => c.id === id);
}

export function getCommandersByRarity(rarity: Rarity): Commander[] {
  return COMMANDERS.filter((c) => c.rarity === rarity);
}

export function getCommandersBySkill(skillName: string): Commander[] {
  return COMMANDERS.filter(
    (c) => c.skill.toLowerCase() === skillName.toLowerCase()
  );
}
